<script>
    function minGuest(el, e) {
        e.preventDefault()
        var value = el.parentElement.querySelector("p")
        var val = parseInt(value.textContent)

        if (val > 1) {
            value.textContent = val - 1

            var room = $("#dd-room .items .room").length

            var guest = 0

            $("#dd-room .items .guest").map(function() {
                guest += parseInt(this.textContent)
            })

            $("#dd-room-btn").val(`${room} Room, ${guest} Guest`)
        }

    }

    function plusGuest(el, e) {
        e.preventDefault()
        var value = el.parentElement.querySelector("p")
        var val = parseInt(value.textContent)
        if (val < 3) {
            value.textContent = val + 1

            var room = $("#dd-room .items .room").length

            var guest = 0

            $("#dd-room .items .guest").map(function() {
                guest += parseInt(this.textContent)
            })

            $("#dd-room-btn").val(`${room} Room, ${guest} Guest`)
        }
    }

    function addRoom() {
        var room = $("#dd-room .items .item").length

        $("#dd-room .items").append(`<div class="d-flex align-items-center justify-content-between px-4 py-3 item">
                            <span>Room <b class="room">${room+1}</b></span>
                            <div class="d-flex align-items-center">
                                <button onclick="minGuest(this)">-</button>
                                <p class="my-0 mx-3 guest">1</p>
                                <button onclick="plusGuest(this)">+</button>
                            </div>
                        </div>`)

        var guest = 0

        $("#dd-room .items .guest").map(function() {
            guest += parseInt(this.textContent)
        })

        $("#dd-room-btn").val(`${room+1} Room, ${guest} Guest`)
    }


    function delRoom() {
        $("#dd-room .items .item").last().remove()

        var room = $("#dd-room .items .room").length

        var guest = 0

        $("#dd-room .items .guest").map(function() {
            guest += parseInt(this.textContent)
        })

        $("#dd-room-btn").val(`${room} Room, ${guest} Guest`)
    }

    $("#dd-room-btn").click(function() {
        $(this).next().toggleClass("show")
    })
</script>